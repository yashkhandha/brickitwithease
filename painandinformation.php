                    <h3>Osteoarthritis</h3>
                    <p>
                        Osteoarthritis can develop due to normal "wear-and-tear" in the wrist, particularly in people who have a family history of arthritis. It is a common problem for many people after they reach middle age, though it may occur in younger people, too.

                        In osteoarthritis, the smooth, slippery articular cartilage that covers the ends of the bones gradually wears away over time. Because the cartilage surface has little to no blood supply, it has little ability to heal or regenerate when it becomes injured or worn down.                    </p>
                    <h3>Posttraumatic Arthritis</h3>
                    <p>
                        Literally meaning &quotspinal narrowing,&quot spinal stenosis can occur when changes in arthritis lead to bony overgrowth of the vertebrae and thickening of the ligaments. This can occur with osteoarthritis or ankylosing spondylitis. If a significant overgrowth occurs, it can cause the spinal column to narrow and press on the nerves housed within. Because the affected nerves have many functions, the condition may cause diverse problems in the lower body, including back pain, pain or numbness in the legs, constipation or urinary incontinence.
                    </p>
                    <h3>Rheumatoid Arthritis</h3>
                    <p>
                        Rheumatoid arthritis is a chronic disease that can affect multiple joints throughout the body. The condition often starts in smaller joints, such as those found in the hand and wrist. It is symmetrical, meaning that it usually affects the same joint on both sides of the body.

                        Rheumatoid arthritis is an autoimmune disease. This means that the body's immune system attacks its own tissues. In rheumatoid arthritis, the defenses that normally protect the body from infection instead damage normal tissue (such as cartilage and ligaments) and can soften bone.                    </p>
              
